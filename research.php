                   	<h3>Research</h3>
                   	<h4>A Study on Berg Furniture by Center for Cultural Studies and Analysis</h4>
                   	<h5>January, 2010</h5>
                   	
                   	<p>When Dr. Margaret King and Jamie O'Boyle witnessed kids making a beeline for some Berg Furniture 
                    	bedroom sets on display in a store, they got interested. Their curiosity was professional, 
                    	since both work for the Center for Cultural Studies and Analysis, a Philadelphia organization that 
                    	does research on consumer behavior.
					</p>  
					<p>
					"The kids sped up as soon as they saw them and went straight up the steps,"said O'Boyle, 
					the center's senior analyst. "I lost count of how many times parents uttered the same phrase" 
					"Why didn't they have beds like this when I was a kid?" I heard it in English and I heard it in 
					Spanish."
					</p>  
					<p>
					"Cognitive science has demonstrated that the emotional centers of the brain respond first, 
					followed by the logic centers. In other words 'facts' are what people use to validate decisions 
					they have already made at an unconscious level," O'Boyle said. The analysts said the Berg pieces 
					create an environment that encourages children to exercise their imaginations.
					</p>  
					<p>
					"Are we saying that this furniture can make your child smarter and more creative? 
					That's what we're looking at." said King. "There are countless research studies that link a creative 
					environment in childhood to success in later life. This furniture encourages and facilitates many 
					of the behaviors that child psychologists believe are necessary for a child to become a well-rounded, 
					confident adult."
					</p>  
					<p>
					"In the children's furniture area, for instance, it won't be enough in the future to simply 
					provide a bed, a dresser, and a desk. Consumers want 'smart' furniture that can enhance and foster 
					their child's psychological as well as their physical needs," King said. "Parents want the smartest 
					thing in the room to be the child, not the Playstation."
					</p>
					
