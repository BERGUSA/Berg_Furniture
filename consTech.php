					<b>Why Do We Use Engineered Wood?</b>
                    
                    <img src="img/ibeam_pic.jpg" style="float:left; padding:10px; margin-top:37px;" />
                    
                    <p>Engineered Wood is a natural and organic product. 
                    Engineered Wood is the result of decades of research and development. 
                    High grade particle Board, Plywood and medium density fiberboard are 
                    designed with attributes not found in solid wood. Engineered Wood provides consistent, 
                    uniform strength. It is highly resistant to warping, cracking, and splitting, whereas 
                    solid wood can have voids, knots, and other imperfections that can't meet our requirements. 
                    High performance standards make Engineered Wood a superior choice for most furniture, 
                    cabinets and architectural applications.
                    </p>
                    
                    <b>How is Engineered Wood Made?</b>
                    
                    <img src="img/logging_pic.jpg" style="float:right; padding: 10px; margin-top:37px;"  />
                    
                    <p>Typically, wood shavings, chips and sawdust that remain after a tree is milled into lumber are 
                    	collected, cleaned and refined to create uniform particles or fibers.  
                    	Excess moisture is removed and an adhesive resin is added.  
                    	The combination of 92% wood and 8% resin, on average, is formed into long thick mats, 
                    	which are then compressed under intense heat and pressure.  
                    	Progress at each stage is closely monitored by computers, which can measure the slightest 
                    	variations that even the human eye can't detect.
                    </p>  
					
					<p>
					The resin which is used today meets all the required standards (such as the new California CARB laws).  
					The resulting wood panels are laboratory-tested for uniformity, strength, screw retention and other 
					structural qualities, as required to meet the stringent American National Standards Institutes 
					requirements for furniture and cabinet grade wood panel products.
					</p> 
 					
 					<b>Why is Engineered Wood Good for the Environment?</b>
 					
					<img src="img/tree_pic.jpg" style="float:left; padding:10px; margin-top:37px;"  />
					
					<p>In the furniture industry, when using solid lumber, only 30% of the tree can be used to make a 
						piece of furniture.  When Engineered Wood is used, more than 95% of the tree can be made into 
						useful consumer products like furniture &amp; cabinets.  That means fewer trees are needed to 
						make these products, less wood tonnage goes into our landfills, and less wood is burned.  
						In most wood furniture and cabinets, Engineered Wood and solid wood are combined to work together, 
