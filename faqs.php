					<b id="whydiscontinue"><u>Why did we decide to discontinue our cribs? </u></b>
					<br /><br />
					<p>Nursery furniture, as a category, has gone through many changes during the last few years. From being a special and unique category it has become a commodity sold at large by big-box retailers. The business has shifted from local factories, experts in wood products, to international factories with lower overhead costs. These lower-priced products, and often lower quality, came to dominate the market and the competition became vicious. This change was not always serving the public's best interest. It was too often that consumers discovered that they traded safety and quality for a lower price. </p>
					<br />
					<p>A quality product, as we view it, is also a safer product. Since we were not willing to compromise the quality of our cribs, a decision was made to phase out this category. In fact, we have decided to sell only the products which can be produced in our NJ factory. As parents ourselves, we did not want to be part of a business that put the profit of the company above all. As you have noticed many cribs have been recalled. The pressure to provide low cost cribs pushed many importers to compromise and substitute expensive but reliable hardware with cheap ones. The government, frustrated with too many complaints and being unable to cope with specific products and individual importers, decided to ban ALL drop gate cribs, good & bad. </p>
